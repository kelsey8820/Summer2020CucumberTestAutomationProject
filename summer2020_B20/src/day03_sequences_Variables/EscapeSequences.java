package day03_sequences_Variables;

public class EscapeSequences {

    public static void main(String[]args){

        System.out.println("Hello Everyone \n My name is Cybertek");
        System.out.println("\tI am in Virginia\n");
        System.out.println("\t\t\t\n\nI love programming");

        System.out.println("\\"); //this prints me one backslash

        System.out.println("\\\\"); //this one prints me two backslash

        System.out.println("/"); // this prints me one front slash

        System.out.println("My name is \'Kelsey\'"); // 'Kelsey'

        System.out.println("My name is \"Kelsey\""); // "Kelsey"







    }





}

